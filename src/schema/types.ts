/**
 * Schema type for validating arrays.
 *
 * Validates that the input is an array and validates each item
 * against the provided item schema.
 *
 * @typeParam T - The type of items in the array
 *
 * @example
 * ```typescript
 * const schema = new ArrayType(new StringType())
 *
 * schema.validate(['a', 'b'])  // { success: true, data: ['a', 'b'] }
 * schema.validate([])          // { success: true, data: [] }
 * schema.validate([1, 2])      // { success: false, errors: [...] }
 * schema.validate('not array') // { success: false, errors: [...] }
 * ```
 */
export class ArrayType<T> extends BaseType<T[]> {
  constructor(private itemType: BaseType<T>) {
    super()
  }

  /**
   * Validates that the value is an array with valid items.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the array data or validation errors
   */
  validate(value: unknown): ValidationResult<T[]> {
    if (!Array.isArray(value)) {
      return this.failure('Expected array', 'invalid_type')
    }

    const errors: ValidationError[] = []
    const result: T[] = []

    for (let i = 0; i < value.length; i++) {
      const itemResult = this.itemType.validate(value[i])
      if (!itemResult.success) {
        // Prepend index to error paths
        itemResult.errors!.forEach((err) => {
          errors.push({ ...err, path: [String(i), ...err.path] })
        })
      } else {
        result.push(itemResult.data!)
      }
    }

    if (errors.length > 0) {
      return this.failures(errors)
    }
    return this.success(result)
  }
}

/**
 * Schema type for validating objects with a defined shape.
 *
 * Validates that the input is an object and validates each property
 * against the corresponding schema in the shape definition.
 * Unknown properties are stripped from the output.
 *
 * @typeParam T - The shape of the object
 *
 * @example
 * ```typescript
 * const schema = new ObjectType({ name: new StringType() })
 *
 * schema.validate({ name: 'John' })        // { success: true, data: { name: 'John' } }
 * schema.validate({ name: 123 })           // { success: false, errors: [...] }
 * schema.validate({})                       // { success: false, errors: [...] }
 * schema.validate({ name: 'John', extra: 1 }) // { success: true, data: { name: 'John' } }
 * ```
 */
export class ObjectType<T extends Record<string, unknown>> extends BaseType<T> {
  constructor(protected shape: { [K in keyof T]: BaseType<T[K]> }) {
    super()
  }

  /**
   * Validates that the value is an object matching the schema shape.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the object data or validation errors
   */
  validate(value: unknown): ValidationResult<T> {
    if (value === null || typeof value !== 'object' || Array.isArray(value)) {
      return this.failure('Expected object', 'invalid_type')
    }

    const obj = value as Record<string, unknown>
    const errors: ValidationError[] = []
    const result: Record<string, unknown> = {}

    for (const [key, schema] of Object.entries(this.shape)) {
      const fieldValue = obj[key]
      if (fieldValue === undefined) {
        errors.push({ path: [key], message: 'Value is required', code: 'invalid_type' })
        continue
      }
      const fieldResult = (schema as BaseType<unknown>).validate(fieldValue)
      if (!fieldResult.success) {
        fieldResult.errors!.forEach((err) => {
          errors.push({ ...err, path: [key, ...err.path] })
        })
      } else {
        result[key] = fieldResult.data
      }
    }

    if (errors.length > 0) {
      return this.failures(errors)
    }
    return this.success(result as T)
  }

  /**
   * Converts this schema to a JSON Schema object.
   *
   * @returns A JSONSchema object representing this schema
   */
  toJSONSchema(): JSONSchema {
    const properties: Record<string, JSONSchema> = {}
    const required: string[] = []

    for (const [key, schema] of Object.entries(this.shape)) {
      properties[key] = this.typeToJSONSchema(schema as BaseType<unknown>)
      required.push(key)
    }

    return {
      type: 'object',
      properties,
      required,
      additionalProperties: false,
    }
  }

  /**
   * Converts a BaseType to its JSON Schema representation.
   */
  protected typeToJSONSchema(type: BaseType<unknown>): JSONSchema {
    let schema: JSONSchema
    if (type instanceof StringType) {
      schema = { type: 'string' }
    } else if (type instanceof NumberType) {
      schema = { type: 'number' }
    } else if (type instanceof BooleanType) {
      schema = { type: 'boolean' }
    } else if (type instanceof ArrayType) {
      schema = { type: 'array' }
    } else if (type instanceof ObjectType) {
      schema = type.toJSONSchema()
    } else {
      schema = { type: 'object' }
    }

    // Add AI hints to $comment if present
    const aiHints = type.getAIHints()
    if (aiHints) {
      schema.$comment = JSON.stringify({ ai: aiHints })
    }

    return schema
  }
}

