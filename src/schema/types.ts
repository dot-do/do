/**
 * Abstract base class for all schema types.
 *
 * Provides common validation patterns, utility methods, and a foundation
 * for method chaining in derived types.
 *
 * @typeParam T - The output type this schema produces after successful validation
 *
 * @example
 * ```typescript
 * class CustomType extends BaseType<MyType> {
 *   validate(value: unknown): ValidationResult<MyType> {
 *     // Custom validation logic
 *   }
 * }
 * ```
 */
export abstract class BaseType<T = unknown> {
  /**
   * Indicates whether this schema handles undefined values (e.g., default, optional).
   * Used by RequiredType to determine whether to check for undefined before or after
   * delegating to the inner schema.
   *
   * @protected
   */
  protected readonly handlesUndefined: boolean = false

  /**
   * AI generation hints for this schema type.
   * @protected
   */
  protected aiHints?: AIMetadata

  /**
   * Description for this schema type (for JSON Schema output).
   * @protected
   */
  protected _description?: string

  /**
   * Example values for this schema type (for JSON Schema output).
   * @protected
   */
  protected _examples?: T[]

  /**
   * Validates the given value against this schema type.
   *
   * @param value - The value to validate (can be any type)
   * @returns A ValidationResult indicating success with typed data or failure with errors
   */
  abstract validate(value: unknown): ValidationResult<T>

  /**
   * Adds AI generation hints to this schema type.
   * Hints are merged with any existing hints.
   *
   * @param hints - AI metadata hints for generation
   * @returns This schema type (for method chaining)
   *
   * @example
   * ```typescript
   * $.string().ai({ description: 'Full name', examples: ['John Doe'] })
   * $.array($.string()).ai({ generate: true, count: 5 })
   * ```
   */
  ai(hints: AIMetadata): this {
    this.aiHints = this.aiHints ? { ...this.aiHints, ...hints } : { ...hints }
    return this
  }

  /**
   * Gets the AI generation hints for this schema type.
   *
   * @returns The AI metadata hints, or undefined if none are set
   */
  getAIHints(): AIMetadata | undefined {
    return this.aiHints
  }

  /**
   * Sets a description for this schema type.
   * The description is included in JSON Schema output.
   *
   * @param description - Human-readable description of this type
   * @returns This schema type (for method chaining)
   *
   * @example
   * ```typescript
   * $.string().describe('Primary email address')
   * ```
   */
  describe(description: string): this {
    this._description = description
    return this
  }

  /**
   * Gets the description for this schema type.
   *
   * @returns The description, or undefined if none is set
   */
  getDescription(): string | undefined {
    return this._description
  }

  /**
   * Adds an example value for this schema type.
   * Examples are included in JSON Schema output.
   *
   * @param value - An example value for this type
   * @returns This schema type (for method chaining)
   *
   * @example
   * ```typescript
   * $.string().example('admin')
   * ```
   */
  example(value: T): this {
    if (!this._examples) {
      this._examples = []
    }
    this._examples.push(value)
    return this
  }

  /**
   * Gets the example values for this schema type.
   *
   * @returns The example values, or undefined if none are set
   */
  getExamples(): T[] | undefined {
    return this._examples
  }

  /**
   * Creates a successful validation result with the validated data.
   *
   * @param data - The validated and typed data
   * @returns A success ValidationResult containing the data
   * @protected
   */
  protected success(data: T): ValidationResult<T> {
    return { success: true, data }
  }

  /**
   * Creates a failed validation result with error details.
   *
   * @param message - Human-readable error message
   * @param code - Machine-readable error code (e.g., 'invalid_type', 'required')
   * @param path - Path to the property that failed (defaults to root [])
   * @returns A failure ValidationResult containing the error
   * @protected
   */
  protected failure(message: string, code: string, path: string[] = []): ValidationResult<T> {
    return {
      success: false,
      errors: [{ path, message, code }],
    }
  }

  /**
   * Creates a failure result with multiple errors.
   *
   * @param errors - Array of validation errors
   * @returns A failure ValidationResult containing all errors
   * @protected
   */
  protected failures(errors: ValidationError[]): ValidationResult<T> {
    return { success: false, errors }
  }

  // ==========================================================================
  // Modifier Methods
  // ==========================================================================

  /**
   * Makes this schema required, rejecting undefined values.
   *
   * @returns A new schema that rejects undefined
   *
   * @example
   * ```typescript
   * $.string().required().validate(undefined)
   * // { success: false, errors: [{ code: 'required', ... }] }
   * ```
   */
  required(): RequiredType<T> {
    return new RequiredType(this)
  }

  /**
   * Makes this schema optional, allowing undefined values.
   *
   * @returns A new schema that accepts undefined
   *
   * @example
   * ```typescript
   * $.string().optional().validate(undefined)
   * // { success: true, data: undefined }
   * ```
   */
  optional(): OptionalType<T> {
    return new OptionalType(this)
  }

  /**
   * Provides a default value when the input is undefined.
   *
   * @param defaultValue - The default value or a function that returns it
   * @returns A new schema that substitutes the default for undefined
   *
   * @example
   * ```typescript
   * $.string().default('unknown').validate(undefined)
   * // { success: true, data: 'unknown' }
   * ```
   */
  default(defaultValue: T | (() => T)): DefaultType<T> {
    return new DefaultType(this, defaultValue)
  }

  /**
   * Transforms the validated value using a function.
   *
   * @typeParam U - The output type after transformation
   * @param fn - The transformation function
   * @returns A new schema that transforms valid values
   *
   * @example
   * ```typescript
   * $.string().transform(s => s.toUpperCase()).validate('hello')
   * // { success: true, data: 'HELLO' }
   * ```
   */
  transform<U>(fn: (value: T) => U): TransformType<T, U> {
    return new TransformType(this, fn)
  }
}

