/**
 * Represents an error that occurred during validation.
 *
 * @example
 * ```typescript
 * const error: ValidationError = {
 *   path: ['user', 'email'],
 *   message: 'Expected string',
 *   code: 'invalid_type'
 * }
 * ```
 */
export interface ValidationError {
  /** The path to the property that failed validation (e.g., ['user', 'email']) */
  path: string[]
  /** Human-readable error message describing the validation failure */
  message: string
  /** Machine-readable error code for programmatic handling */
  code: string
}

/**
 * Discriminated union result type for validation operations.
 * Either succeeds with typed data or fails with an array of errors.
 *
 * @typeParam T - The expected type of the validated data
 *
 * @example
 * ```typescript
 * const result: ValidationResult<string> = schema.validate(input)
 * if (result.success) {
 *   console.log(result.data) // typed as string
 * } else {
 *   console.error(result.errors) // ValidationError[]
 * }
 * ```
 */
export type ValidationResult<T = unknown> =
  | { success: true; data: T; errors?: undefined }
  | { success: false; data?: undefined; errors: ValidationError[] }

