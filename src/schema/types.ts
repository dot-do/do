/**
 * Schema type for building object schemas with fluent API.
 *
 * Provides methods like strict(), passthrough(), partial(), pick(), omit(),
 * extend(), merge(), and required() for composing schemas.
 *
 * @typeParam T - The shape of the object
 *
 * @example
 * ```typescript
 * const schema = new SchemaType({ name: new StringType() })
 *   .strict()
 *   .partial()
 *
 * schema.validate({ name: 'John' })  // { success: true, data: { name: 'John' } }
 * ```
 */
export class SchemaType<T extends Record<string, unknown>> extends BaseType<T> {
  private strictMode = false
  private passthroughMode = false
  private partialMode = false

  constructor(private shape: { [K in keyof T]: BaseType<T[K]> }) {
    super()
  }

  /**
   * Validates that the value is an object matching the schema shape.
   */
  validate(value: unknown): ValidationResult<T> {
    if (value === null || typeof value !== 'object' || Array.isArray(value)) {
      return this.failure('Expected object', 'invalid_type')
    }

    const obj = value as Record<string, unknown>
    const errors: ValidationError[] = []
    const result: Record<string, unknown> = {}

    // Check for unrecognized keys in strict mode
    if (this.strictMode) {
      const schemaKeys = new Set(Object.keys(this.shape))
      for (const key of Object.keys(obj)) {
        if (!schemaKeys.has(key) && key !== '__proto__') {
          errors.push({ path: [key], message: `Unrecognized key: ${key}`, code: 'unrecognized_keys' })
        }
      }
    }

    for (const [key, schema] of Object.entries(this.shape)) {
      const fieldValue = obj[key]
      if (fieldValue === undefined) {
        if (!this.partialMode) {
          errors.push({ path: [key], message: 'Value is required', code: 'required' })
        }
        continue
      }
      const fieldResult = (schema as BaseType<unknown>).validate(fieldValue)
      if (!fieldResult.success) {
        fieldResult.errors!.forEach((err) => {
          errors.push({ ...err, path: [key, ...err.path] })
        })
      } else {
        result[key] = fieldResult.data
      }
    }

    // Passthrough extra properties
    if (this.passthroughMode) {
      for (const key of Object.keys(obj)) {
        if (!(key in this.shape) && key !== '__proto__') {
          result[key] = obj[key]
        }
      }
    }

    if (errors.length > 0) {
      return this.failures(errors)
    }
    return this.success(result as T)
  }

  /**
   * Makes the schema reject objects with unknown keys.
   */
  strict(): SchemaType<T> {
    const clone = this.clone()
    clone.strictMode = true
    return clone
  }

  /**
   * Makes the schema pass through unknown keys to the output.
   */
  passthrough(): SchemaType<T> {
    const clone = this.clone()
    clone.passthroughMode = true
    return clone
  }

  /**
   * Makes all fields optional.
   */
  partial(): SchemaType<Partial<T>> {
    const clone = this.clone() as unknown as SchemaType<Partial<T>>
    clone.partialMode = true
    return clone
  }

  /**
   * Makes all fields required (reverses partial()).
   */
  required(): SchemaType<Required<T>> {
    const clone = this.clone() as unknown as SchemaType<Required<T>>
    clone.partialMode = false
    return clone
  }

  /**
   * Creates a new schema with only the specified keys.
   */
  pick<K extends keyof T>(keys: K[]): SchemaType<Pick<T, K>> {
    const newShape: Record<string, BaseType<unknown>> = {}
    for (const key of keys) {
      if (key in this.shape) {
        newShape[key as string] = this.shape[key] as BaseType<unknown>
      }
    }
    const clone = new SchemaType(newShape as { [P in K]: BaseType<T[P]> })
    clone.strictMode = this.strictMode
    clone.passthroughMode = this.passthroughMode
    clone.partialMode = this.partialMode
    return clone as unknown as SchemaType<Pick<T, K>>
  }

  /**
   * Creates a new schema without the specified keys.
   */
  omit<K extends keyof T>(keys: K[]): SchemaType<Omit<T, K>> {
    const keysToOmit = new Set(keys as string[])
    const newShape: Record<string, BaseType<unknown>> = {}
    for (const [key, schema] of Object.entries(this.shape)) {
      if (!keysToOmit.has(key)) {
        newShape[key] = schema as BaseType<unknown>
      }
    }
    const clone = new SchemaType(newShape as { [P in Exclude<keyof T, K>]: BaseType<T[P]> })
    clone.strictMode = this.strictMode
    clone.passthroughMode = this.passthroughMode
    clone.partialMode = this.partialMode
    return clone as unknown as SchemaType<Omit<T, K>>
  }

  /**
   * Creates a new schema with additional fields.
   */
  extend<U extends Record<string, unknown>>(
    shape: { [K in keyof U]: BaseType<U[K]> }
  ): SchemaType<T & U> {
    const newShape = { ...this.shape, ...shape } as { [K in keyof (T & U)]: BaseType<(T & U)[K]> }
    const clone = new SchemaType(newShape)
    clone.strictMode = this.strictMode
    clone.passthroughMode = this.passthroughMode
    clone.partialMode = this.partialMode
    return clone
  }

  /**
   * Merges this schema with another schema.
   */
  merge<U extends Record<string, unknown>>(other: SchemaType<U>): SchemaType<T & U> {
    const newShape = { ...this.shape, ...other.shape } as { [K in keyof (T & U)]: BaseType<(T & U)[K]> }
    const clone = new SchemaType(newShape)
    clone.strictMode = this.strictMode || other.strictMode
    clone.passthroughMode = this.passthroughMode || other.passthroughMode
    clone.partialMode = this.partialMode && other.partialMode
    return clone
  }

  private clone(): SchemaType<T> {
    const clone = new SchemaType(this.shape)
    clone.strictMode = this.strictMode
    clone.passthroughMode = this.passthroughMode
    clone.partialMode = this.partialMode
    return clone
  }

  // ==========================================================================
  // Schema Introspection Methods
  // ==========================================================================

  /**
   * Returns the list of field names in this schema.
   *
   * @returns An array of field names
   *
   * @example
   * ```typescript
   * const schema = $.schema({ name: $.string(), age: $.number() })
   * schema.getFields() // ['name', 'age']
   * ```
   */
  getFields(): (keyof T)[] {
    return Object.keys(this.shape) as (keyof T)[]
  }

  /**
   * Returns the type validator for a specific field.
   *
   * @param name - The field name to get the type for
   * @returns The BaseType validator for the field, or undefined if not found
   *
   * @example
   * ```typescript
   * const schema = $.schema({ name: $.string(), age: $.number() })
   * schema.getFieldType('name') // StringType instance
   * schema.getFieldType('unknown') // undefined
   * ```
   */
  getFieldType<K extends keyof T>(name: K): BaseType<T[K]> | undefined {
    return this.shape[name]
  }

  /**
   * Checks if a field is required (i.e., not in partial mode).
   *
   * @param name - The field name to check
   * @returns true if the field is required, false if optional or not found
   *
   * @example
   * ```typescript
   * const schema = $.schema({ name: $.string() })
   * schema.isRequired('name') // true
   * schema.partial().isRequired('name') // false
   * ```
   */
  isRequired<K extends keyof T>(name: K): boolean {
    if (!(name in this.shape)) {
      return false
    }
    return !this.partialMode
  }

  /**
   * Returns the full shape definition of this schema.
   *
   * @returns The shape object mapping field names to their validators
   *
   * @example
   * ```typescript
   * const schema = $.schema({ name: $.string(), age: $.number() })
   * const shape = schema.getShape()
   * // { name: StringType, age: NumberType }
   * ```
   */
  getShape(): { [K in keyof T]: BaseType<T[K]> } {
    return { ...this.shape }
  }

  /**
   * Converts this schema to a JSON Schema object.
   */
  toJSONSchema(): JSONSchema {
    const properties: Record<string, JSONSchema> = {}
    const required: string[] = []

    for (const [key, schema] of Object.entries(this.shape)) {
      properties[key] = this.typeToJSONSchema(schema as BaseType<unknown>)
      if (!this.partialMode) {
        required.push(key)
      }
    }

    const result: JSONSchema = {
      $schema: 'https://json-schema.org/draft/2020-12/schema',
      type: 'object',
      properties,
      required,
      additionalProperties: false,
    }

    // Add schema-level description if present
    if (this._description) {
      result.description = this._description
    }

    return result
  }

  private typeToJSONSchema(type: BaseType<unknown>): JSONSchema {
    let schema: JSONSchema
    if (type instanceof StringType) {
      schema = { type: 'string' }
    } else if (type instanceof NumberType) {
      schema = { type: 'number' }
    } else if (type instanceof BooleanType) {
      schema = { type: 'boolean' }
    } else if (type instanceof ArrayType) {
      schema = { type: 'array' }
    } else if (type instanceof ObjectType || type instanceof SchemaType) {
      schema = (type as SchemaType<Record<string, unknown>>).toJSONSchema()
    } else {
      schema = { type: 'object' }
    }

    // Add description if present
    const description = type.getDescription()
    if (description) {
      schema.description = description
    }

    // Add examples if present
    const examples = type.getExamples()
    if (examples && examples.length > 0) {
      schema.examples = examples
    }

    // Add AI hints to $comment if present
    const aiHints = type.getAIHints()
    if (aiHints) {
      schema.$comment = JSON.stringify({ ai: aiHints })
    }

    return schema
  }
}
