/**
 * Reference matching mode for $ref validation.
 *
 * - 'exact': Strict matching, rejects additional properties
 * - 'fuzzy': Loose matching, allows additional properties and hints
 */
export type ReferenceMode = 'exact' | 'fuzzy'

/**
 * Direction of the relationship reference.
 *
 * - 'forward': Outgoing relationship (this -> target)
 * - 'backward': Incoming relationship (target -> this)
 */
export type ReferenceDirection = 'forward' | 'backward'

/**
 * Parsed operator result for RefType.
 */
export interface RefOperator {
  direction: ReferenceDirection
  mode: ReferenceMode
  type: string
  isArray: boolean
}

/**
 * Shape of a reference object with $ref property.
 */
export interface RefObject {
  $ref: string
  [key: string]: unknown
}

/**
 * Regex pattern for parsing relationship operators in $.ref()
 * Matches: operator (-> | ~> | <- | <~) + PascalCase type name + optional array marker
 */
const REF_OPERATOR_REGEX = /^(->|~>|<-|<~)([A-Z][a-zA-Z0-9]*)(\[\])?$/

/**
 * Direction mapping for each operator
 */
const REF_DIRECTION_MAP: Record<string, ReferenceDirection> = {
  '->': 'forward',
  '~>': 'forward',
  '<-': 'backward',
  '<~': 'backward',
}

/**
 * Mode mapping for each operator
 */
const REF_MODE_MAP: Record<string, ReferenceMode> = {
  '->': 'exact',
  '~>': 'fuzzy',
  '<-': 'exact',
  '<~': 'fuzzy',
}

/**
 * Schema type for validating reference objects.
 *
 * References are objects with a $ref property that points to another resource.
 * Supports relationship operators:
 * - `->User` = forward exact relationship to User
 * - `~>User` = forward fuzzy (AI-discovered) relationship
 * - `<-User` = backward relationship from User
 * - `<~User` = backward fuzzy relationship
 * - `->User[]` = array of relationships
 *
 * Supports two modes:
 * - exact: Only $ref property allowed, strict type matching
 * - fuzzy: Additional properties allowed, supports semantic hints
 *
 * @example
 * ```typescript
 * const schema = new RefType('->User')
 *
 * // Exact mode (default)
 * schema.validate({ $ref: 'https://example.com/user/123' })
 * // { success: true, data: { $ref: '...' } }
 *
 * // Fuzzy mode allows extra properties
 * $.ref('~>Tag').validate({ $ref: '...', confidence: 0.9 })
 * // { success: true, data: { $ref: '...', confidence: 0.9 } }
 * ```
 */
export class RefType extends BaseType<RefObject | RefObject[]> {
  private mode: ReferenceMode = 'exact'
  private direction: ReferenceDirection = 'forward'
  private _isArray: boolean = false
  private _typeName: string

  /**
   * Creates a new RefType for the given type name or operator string.
   *
   * @param typeNameOrOperator - The type name (e.g., 'User') or operator string (e.g., '->User', '~>Tag[]')
   */
  constructor(typeNameOrOperator: string) {
    super()
    this._typeName = typeNameOrOperator
    this.parseOperator(typeNameOrOperator)
  }

  /**
   * Parse the operator string to extract direction, mode, type, and array flag.
   */
  private parseOperator(input: string): void {
    const match = input.match(REF_OPERATOR_REGEX)
    if (match) {
      const [, op, type, arrayMarker] = match
      this.direction = REF_DIRECTION_MAP[op]
      this.mode = REF_MODE_MAP[op]
      this._typeName = type
      this._isArray = !!arrayMarker
    } else {
      // Plain type name - default to forward exact
      this.direction = 'forward'
      this.mode = 'exact'
      this._isArray = false
    }
  }

  /**
   * Gets the type name this reference points to.
   *
   * @returns The type name string (without operator prefix)
   */
  getTypeName(): string {
    return this._typeName
  }

  /**
   * Gets the current reference matching mode.
   *
   * @returns The current mode ('exact' or 'fuzzy')
   */
  getMode(): ReferenceMode {
    return this.mode
  }

  /**
   * Gets the relationship direction.
   *
   * @returns The direction ('forward' or 'backward')
   */
  getDirection(): ReferenceDirection {
    return this.direction
  }

  /**
   * Returns whether this reference type expects an array.
   *
   * @returns true if array type, false otherwise
   */
  isArray(): boolean {
    return this._isArray
  }

  /**
   * Gets the parsed operator information.
   *
   * @returns The operator components
   */
  getOperator(): RefOperator {
    return {
      direction: this.direction,
      mode: this.mode,
      type: this._typeName,
      isArray: this._isArray,
    }
  }

  /**
   * Returns a new RefType with exact matching mode.
   *
   * In exact mode:
   * - Only the $ref property is allowed
   * - Additional properties cause validation failure
   *
   * @returns A new RefType configured for exact matching
   */
  exact(): RefType {
    const clone = this.clone()
    clone.mode = 'exact'
    return clone
  }

  /**
   * Returns a new RefType with fuzzy matching mode.
   *
   * In fuzzy mode:
   * - Additional properties are allowed
   * - Supports semantic hints like $type
   *
   * @returns A new RefType configured for fuzzy matching
   */
  fuzzy(): RefType {
    const clone = this.clone()
    clone.mode = 'fuzzy'
    return clone
  }

  /**
   * Creates a clone of this RefType with all current settings.
   */
  private clone(): RefType {
    const clone = new RefType(this._typeName)
    clone.mode = this.mode
    clone.direction = this.direction
    clone._isArray = this._isArray
    clone._description = this._description
    return clone
  }

  /**
   * Validates a single reference object.
   */
  private validateSingleRef(value: unknown): ValidationResult<RefObject> {
    if (value === null || typeof value !== 'object' || Array.isArray(value)) {
      return this.failure(`Expected ${this._typeName} reference object`, 'invalid_type')
    }

    const obj = value as Record<string, unknown>

    if (!('$ref' in obj)) {
      return this.failure(`Expected $ref property for ${this._typeName}`, 'invalid_ref')
    }

    if (typeof obj.$ref !== 'string') {
      return this.failure('$ref must be a string', 'invalid_ref_type')
    }

    if (obj.$ref === '') {
      return this.failure('$ref cannot be empty', 'invalid_ref_empty')
    }

    // In exact mode, reject additional properties
    if (this.mode === 'exact') {
      const extraKeys = Object.keys(obj).filter((k) => k !== '$ref')
      if (extraKeys.length > 0) {
        return this.failure(`Unexpected property: ${extraKeys[0]}`, 'unexpected_property')
      }
    }

    return { success: true, data: obj as RefObject }
  }

  /**
   * Validates that the value is a valid reference object or array of references.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the RefObject(s) or validation errors
   */
  validate(value: unknown): ValidationResult<RefObject | RefObject[]> {
    if (this._isArray) {
      // Array validation
      if (!Array.isArray(value)) {
        return this.failure(`Expected array of ${this._typeName} references`, 'invalid_type')
      }

      const results: RefObject[] = []
      const errors: ValidationError[] = []

      for (let i = 0; i < value.length; i++) {
        const itemResult = this.validateSingleRef(value[i])
        if (itemResult.success) {
          results.push(itemResult.data)
        } else {
          // Prepend index to error paths
          itemResult.errors!.forEach((err) => {
            errors.push({ ...err, path: [String(i), ...err.path] })
          })
        }
      }

      if (errors.length > 0) {
        return { success: false, errors }
      }

      return { success: true, data: results }
    }

    // Single reference validation
    return this.validateSingleRef(value)
  }

  /**
   * Converts this RefType to JSON Schema representation.
   *
   * @returns A JSONSchema object with DO metadata extensions
   */
  toJSONSchema(): JSONSchema & Record<string, unknown> {
    const refSchema: JSONSchema & Record<string, unknown> = {
      type: 'object',
      properties: {
        $ref: { type: 'string' },
      },
      required: ['$ref'],
      additionalProperties: this.mode === 'fuzzy',
    }

    // Add AI/DO metadata
    refSchema['x-do-mode'] = this.mode
    refSchema['x-do-direction'] = this.direction
    refSchema['x-do-target-type'] = this._typeName

    if (this.mode === 'fuzzy') {
      refSchema['x-do-ai-discoverable'] = true
    }

    if (this._description) {
      refSchema.description = this._description
    }

    if (this._isArray) {
      return {
        type: 'array',
        items: refSchema,
        'x-do-mode': this.mode,
        'x-do-direction': this.direction,
        'x-do-target-type': this._typeName,
        'x-do-ai-discoverable': this.mode === 'fuzzy' ? true : undefined,
      }
    }

    return refSchema
  }
}

