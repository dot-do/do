/**
 * Schema type that requires a value to be present (not undefined).
 *
 * Wraps another schema and rejects undefined values before
 * delegating to the inner schema for type validation.
 *
 * @typeParam T - The output type from the inner schema
 *
 * @example
 * ```typescript
 * const schema = new RequiredType(new StringType())
 *
 * schema.validate('hello')    // { success: true, data: 'hello' }
 * schema.validate(undefined)  // { success: false, errors: [{ code: 'required', ... }] }
 * ```
 */
export class RequiredType<T> extends BaseType<T> {
  constructor(private readonly inner: BaseType<T>) {
    super()
  }

  /**
   * Validates that the value is not undefined, with special handling for
   * inner schemas that handle undefined (like default() or optional()).
   *
   * - If inner schema handles undefined: delegate first, then check result
   * - Otherwise: check for undefined first, then delegate
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the validated data or errors
   */
  validate(value: unknown): ValidationResult<T> {
    // Check if the inner schema handles undefined values (default, optional)
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    const innerHandlesUndefined = (this.inner as any).handlesUndefined === true

    if (innerHandlesUndefined) {
      // Let inner schema process the value first (allows default() to work)
      const result = this.inner.validate(value)

      // If inner validation failed, propagate the error
      if (!result.success) {
        return result
      }

      // Check if the result data is undefined (required check on output)
      if (result.data === undefined) {
        return this.failure('Value is required', 'required')
      }

      return result
    } else {
      // Check for undefined before delegating to inner schema
      if (value === undefined) {
        return this.failure('Value is required', 'required')
      }
      return this.inner.validate(value)
    }
  }
}

/**
 * Schema type that allows undefined values.
 *
 * Wraps another schema and passes undefined through without validation,
 * while delegating non-undefined values to the inner schema.
 *
 * @typeParam T - The output type from the inner schema
 *
 * @example
 * ```typescript
 * const schema = new OptionalType(new StringType())
 *
 * schema.validate('hello')    // { success: true, data: 'hello' }
 * schema.validate(undefined)  // { success: true, data: undefined }
 * schema.validate(123)        // { success: false, errors: [...] }
 * ```
 */
export class OptionalType<T> extends BaseType<T | undefined> {
  protected override readonly handlesUndefined = true

  constructor(private readonly inner: BaseType<T>) {
    super()
  }

  /**
   * Passes undefined through, otherwise delegates to inner schema.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the validated data or errors
   */
  validate(value: unknown): ValidationResult<T | undefined> {
    if (value === undefined) {
      return this.success(undefined)
    }
    return this.inner.validate(value) as ValidationResult<T | undefined>
  }
}

/**
 * Schema type that provides a default value when input is undefined.
 *
 * Wraps another schema and substitutes a default value for undefined,
 * while delegating non-undefined values to the inner schema.
 *
 * @typeParam T - The output type from the inner schema
 *
 * @example
 * ```typescript
 * const schema = new DefaultType(new StringType(), 'unknown')
 *
 * schema.validate('hello')    // { success: true, data: 'hello' }
 * schema.validate(undefined)  // { success: true, data: 'unknown' }
 * schema.validate(123)        // { success: false, errors: [...] }
 * ```
 */
export class DefaultType<T> extends BaseType<T> {
  protected override readonly handlesUndefined = true

  constructor(
    private readonly inner: BaseType<T>,
    private readonly defaultValue: T | (() => T)
  ) {
    super()
  }

  /**
   * Returns default value for undefined, otherwise delegates to inner schema.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the validated data or errors
   */
  validate(value: unknown): ValidationResult<T> {
    if (value === undefined) {
      const resolved =
        typeof this.defaultValue === 'function'
          ? (this.defaultValue as () => T)()
          : this.defaultValue
      return this.success(resolved)
    }
    return this.inner.validate(value)
  }
}

/**
 * Schema type that transforms validated values using a function.
 *
 * Wraps another schema and applies a transformation function to
 * successfully validated values. Invalid values are not transformed.
 *
 * @typeParam T - The input type from the inner schema
 * @typeParam U - The output type after transformation
 *
 * @example
 * ```typescript
 * const schema = new TransformType(new StringType(), s => s.toUpperCase())
 *
 * schema.validate('hello')  // { success: true, data: 'HELLO' }
 * schema.validate(123)      // { success: false, errors: [...] }
 * ```
 */
export class TransformType<T, U> extends BaseType<U> {
  constructor(
    private readonly inner: BaseType<T>,
    private readonly fn: (value: T) => U
  ) {
    super()
  }

  /**
   * Validates with inner schema, then transforms successful results.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the transformed data or errors
   */
  validate(value: unknown): ValidationResult<U> {
    const result = this.inner.validate(value)
    if (!result.success) {
      return result as unknown as ValidationResult<U>
    }
    return this.success(this.fn(result.data))
  }
}

