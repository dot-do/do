/**
 * Schema type for validating string values.
 *
 * Validates that the input is a JavaScript string primitive.
 * Empty strings are considered valid.
 *
 * @example
 * ```typescript
 * const schema = new StringType()
 *
 * schema.validate('hello')  // { success: true, data: 'hello' }
 * schema.validate('')       // { success: true, data: '' }
 * schema.validate(123)      // { success: false, errors: [...] }
 * schema.validate(null)     // { success: false, errors: [...] }
 * ```
 */
export class StringType extends BaseType<string> {
  /**
   * Validates that the value is a string.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the string data or validation errors
   */
  validate(value: unknown): ValidationResult<string> {
    if (typeof value !== 'string') {
      return this.failure('Expected string', 'invalid_type')
    }
    return this.success(value)
  }
}

/**
 * Schema type for validating number values.
 *
 * Validates that the input is a JavaScript number primitive.
 * NaN is rejected, but Infinity and -Infinity are accepted.
 *
 * @example
 * ```typescript
 * const schema = new NumberType()
 *
 * schema.validate(42)       // { success: true, data: 42 }
 * schema.validate(3.14)     // { success: true, data: 3.14 }
 * schema.validate(0)        // { success: true, data: 0 }
 * schema.validate(-5)       // { success: true, data: -5 }
 * schema.validate(Infinity) // { success: true, data: Infinity }
 * schema.validate(NaN)      // { success: false, errors: [...] }
 * schema.validate('42')     // { success: false, errors: [...] }
 * ```
 */
export class NumberType extends BaseType<number> {
  /**
   * Validates that the value is a number (not NaN).
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the number data or validation errors
   */
  validate(value: unknown): ValidationResult<number> {
    if (typeof value !== 'number' || Number.isNaN(value)) {
      return this.failure('Expected number', 'invalid_type')
    }
    return this.success(value)
  }
}

/**
 * Schema type for validating boolean values.
 *
 * Validates that the input is a JavaScript boolean primitive.
 * Does not coerce truthy/falsy values.
 *
 * @example
 * ```typescript
 * const schema = new BooleanType()
 *
 * schema.validate(true)     // { success: true, data: true }
 * schema.validate(false)    // { success: true, data: false }
 * schema.validate(1)        // { success: false, errors: [...] }
 * schema.validate('true')   // { success: false, errors: [...] }
 * ```
 */
export class BooleanType extends BaseType<boolean> {
  /**
   * Validates that the value is a boolean.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the boolean data or validation errors
   */
  validate(value: unknown): ValidationResult<boolean> {
    if (typeof value !== 'boolean') {
      return this.failure('Expected boolean', 'invalid_type')
    }
    return this.success(value)
  }
}

/**
 * Schema type for validating Date values.
 *
 * Validates Date objects and coerces ISO date strings to Date objects.
 * Invalid Date objects (where getTime() returns NaN) are rejected.
 *
 * @example
 * ```typescript
 * const schema = new DateType()
 *
 * schema.validate(new Date())            // { success: true, data: Date }
 * schema.validate('2024-01-15')          // { success: true, data: Date } (coerced)
 * schema.validate('2024-01-15T12:00:00Z') // { success: true, data: Date } (coerced)
 * schema.validate(new Date('invalid'))   // { success: false, errors: [...] }
 * schema.validate('not-a-date')          // { success: false, errors: [...] }
 * schema.validate(123)                   // { success: false, errors: [...] }
 * ```
 */
export class DateType extends BaseType<Date> {
  /**
   * Validates that the value is a valid Date or coercible ISO string.
   *
   * @param value - The value to validate
   * @returns A ValidationResult with the Date data or validation errors
   */
  validate(value: unknown): ValidationResult<Date> {
    // Handle Date objects
    if (value instanceof Date) {
      if (Number.isNaN(value.getTime())) {
        return this.failure('Expected valid date', 'invalid_type')
      }
      return this.success(value)
    }

    // Handle ISO date strings (coercion)
    if (typeof value === 'string') {
      const date = new Date(value)
      if (Number.isNaN(date.getTime())) {
        return this.failure('Expected valid date', 'invalid_type')
      }
      return this.success(date)
    }

    return this.failure('Expected date or ISO date string', 'invalid_type')
  }
}

